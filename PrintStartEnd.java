/*
 Start number:9
Ending number: 26
 
o/p:display values between start and ending number
 
 */

/**
 * @author bikas
 *
 */

public class PrintStartEnd {

	public static void main(String[] args) {
		int start = 9;
		int end = 26;
		for (int i = start; i <= end; i++) {
			System.out.print(i + " ");
		}

	}

}
